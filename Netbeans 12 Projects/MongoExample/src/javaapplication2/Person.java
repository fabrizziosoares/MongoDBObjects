package javaapplication2;

import helper.dao.BasicEntity;

/**
 *
 * @author fabrizzio
 */
public class Person implements BasicEntity {

    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
    private int id;
    private String name;
    private int age;
    
    
}
